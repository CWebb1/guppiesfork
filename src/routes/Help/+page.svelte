    <h2>Nutri-Scores</h2>
    <p>
      Nutri-Scores are a grading of food and drink products of their nutritional
      quality. These are assigned from A for the most healthy products to E for
      the least healthy, and everything in between. Nutri-Scores are determined
      from the nutritional information found on packaging, and calculated by
      Open Food Facts. Negative factors, such as fat, sugar and high levels of
      salt are considered, as well as positive factors such as fruits and
      vegetables and more. The final Nutri-Score is available online via the <a
        href="https://nz.openfoodfacts.org/">Open Food Facts</a
      > website or labelled on products in some countries (not widely in New Zealand).
    </p>
    <p>
      <a href="https://nz.openfoodfacts.org/nutriscore">Click here</a> to find
      out more about Nutri-Scores.
    </p>
